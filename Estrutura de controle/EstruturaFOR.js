/**
 * A estrutura 'for' é uma das estruturas de controle de repetição mais usadas em JavaScript.
 * Ela permite executar um bloco de código várias vezes com base em uma condição. A
 * estrutura 'for' inclui três partes: inicialização, condição e expressão de atualização.
 * Aqui está a sintaxe da estrutura 'for':
 */
for (inicializacao; condicao; expressaoAtualizacao) {
    // Código a ser executado enquanto a condição for verdadeira
}

/**
 * Inicialização: 
 * É onde você configura o valor inicial de uma variável que será usada para controlar o loop.
 */

/**
 * Condição: 
 * É uma expressão que é avaliada antes de cada iteração do loop. 
 * Se for verdadeira, o loop continua; caso contrário, ele sai.
 */

/**
 * Expressão de Atualização: 
 * É onde você atualiza o valor da variável de controle após cada iteração.
 */

// Exemplo:
for (let i = 0; i < 5; i++) {
    console.log("Contagem:", i);
}

/**
 * Neste exemplo, o loop 'for' imprimirá os valores de 'i' de 0 a 4. A cada iteração, o valor de
 * 'i' é incrementado em 1.
 */

/**
 * A estrutura 'for' é particularmente útil quando você sabe quantas vezes deseja executar 
 * um bloco de código. No entanto, ela também pode ser usada para repetições baseadas em
 * condições, desde que a condição seja gerenciada adequadamente nas partes de 
 * inicialização, condição e expressão de atualização.
 */